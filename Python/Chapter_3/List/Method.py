list = [2, 1, 3]
list.append(4) #add one element at the end
print(list)

list.sort()#sort in ascending order
print(list)

list.sort(reverse=True)#sort in descending order
print(list)

list.reverse()#reverse list
print(list)

list.insert(2,5)#insert element at index
print(list)

list.remove(1)#remove first occurrence of element
print(list)

list.pop(0)#remove element at index
print(list)